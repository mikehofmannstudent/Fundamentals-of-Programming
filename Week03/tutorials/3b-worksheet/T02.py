import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# add each corresponding element
c = a + b
print(c)

# add each element by 1
c = a + 1
print(c)

# substract each corresponding element
c = a - b
print(c)

# multiply each corresponding element
c = a * b
print(c)

# divide each corresponsing element
c = a / b
print(c)

# note for + - * /, the size of the two arrays must be the same
