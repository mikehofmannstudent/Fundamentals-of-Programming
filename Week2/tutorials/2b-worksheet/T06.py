# for apostrophe
# use \ to escape an apostrophe
str1 = 'It\'s just a cat!'

# or use double quotation
str2 = "It's a dog."

print(str1)
print(str2)

# for quotation
# use \ to escape quotation
str3 = "My favorite language is \"Python\"."

print (str3)

# or use apostrophe
str4 = '"C++" is tough!'

print(str4)
