num1 = 2
num2 = 3

# this is true, 2 == 2 => true
print(num1 == 2)

# this is true, 2 != 3 => true
print(num1 != 3)

# this is false, 3 == 2 => false
print(num2 == 2)

# this is false, 3 != 3 => false
print(num2 != 3)

# this is false, 2 < 2 => false
print(num1 < 2)

# this is true, 2 <= 2 => true
print(num1 <= 2)

# this is true, 2 < 3 => true
print(num1 < num2)

# this is false, 2 > 3 => false
print(num1 > num2)