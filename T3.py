num1 = 2
num2 = 3

# plus
total = num1 + num2

# minus
diff = num1 - num2

# multiplication
product = num1 * num2

# divide
division = num1 / num2

# power
power = num1 ** num2

# output
print(f"{num1} + {num2} = {total}")
print(f"{num1} - {num2} = {diff}")
print(f"{num1} * {num2}) = {product}")
print(f"{num1} / {num2} = {division:.2}")
print(f"{num1} ** {num2} = {power}")